# 1. 提取模数 d1 和目标范数 d2
d1 = 14218340361227255807
d2 = 11890258217623798580

# 2. 提取计算出的 Gram 矩阵 (M_Gram)
M = [
    [6132285984130714, 6685364712869641032, 6591013538782871415, 680747469605702040],
    [6685364712869641032, 5194968178623608343, 8323313795219436867, 1691821574403242226],
    [6591013538782871415, 8323313795219436867, 3100913147385832405, 5510597254890603982],
    [680747469605702040, 1691821574403242226, 5510597254890603982, 7421898533410955212]
]

# 3. 提取你算出的向量 u 和 w
u = [8781866460914734680, 10452602578764437627, 6232214289782291421, 13138060130954140054]
w = [551858178686380996, 183076514754059782, 13663674645590054233, 1395122817467994373]

# 4. 提取 HNF 矩阵第一列的系数 (用于验证 w 生成的子格)
h1 = 7763565243056189579
h2 = 7196694429571068544
h3 = 13371457556105949309

print("="*40)
print("开始执行数学验证...")
print("="*40)

# 验证 0: 检查 Gram 矩阵是否是对称的
is_symmetric = all(M[i][j] == M[j][i] for i in range(4) for j in range(4))
print(f"0. Gram 矩阵是对称的吗?         : {'✅ 通过' if is_symmetric else '❌ 失败'}")

# 验证 1: Q(u) = u * M * u^T mod d1 == d2
Qu = sum(u[i] * M[i][j] * u[j] for i in range(4) for j in range(4)) % d1
print(f"1. u 的范数映射正确吗 (Q(u)==d2)? : {'✅ 通过' if Qu == d2 else '❌ 失败'} (计算出: {Qu}, 目标: {d2})")

# 验证 2: Q(w) = w * M * w^T mod d1 == 0  (w 必须是各向同性向量)
Qw = sum(w[i] * M[i][j] * w[j] for i in range(4) for j in range(4)) % d1
print(f"2. w 是各向同性向量吗 (Q(w)==0)?  : {'✅ 通过' if Qw == 0 else '❌ 失败'} (Qw = {Qw})")

# 验证 3: B(u, w) = u * (M + M^T) * w^T mod d1 == 0 (u 和 w 必须相互正交)
Buw = sum(u[i] * (M[i][j] + M[j][i]) * w[j] for i in range(4) for j in range(4)) % d1
print(f"3. u 和 w 正交吗 (B(u,w)==0)?     : {'✅ 通过' if Buw == 0 else '❌ 失败'} (Buw = {Buw})")

# 验证 4: HNF 是否真的是基于 w 正确构造的
w_hnf_check = [
    (w[0] * h1) % d1 == w[1],
    (w[0] * h2) % d1 == w[2],
    (w[0] * h3) % d1 == w[3]
]
print(f"4. HNF 构造与 w 匹配吗?           : {'✅ 通过' if all(w_hnf_check) else '❌ 失败'}")

# 验证 5: 顺便检查 LLL 约化的向量长度是否符合 Minkowski 预期
